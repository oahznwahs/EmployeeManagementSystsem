    private void SearchForEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchForEmployeeActionPerformed
        // TODO add your handling code here:
        boolean invalid = false;
        int searchInput = -1;
        DefaultListModel model = new DefaultListModel();
        try {
            searchInput = Integer.parseInt(Search_Input.getText());
            System.out.println(searchInput);
        } catch (NumberFormatException NumberFormatException) {
            invalid = true;
        }

        if (invalid == true) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
        } else {
            int targetIndex = theHT.searchTable(searchInput);
            System.out.println(targetIndex);

            if (targetIndex == -1) {
                model.addElement("Employee not found");
                Search_Display.setModel(model);
            } else {
                int targetBucket = theHT.calcBucket(searchInput);
                System.out.println(targetBucket);
                EmployeeInfo someEmployee = theHT.buckets[targetBucket].get(targetIndex);

                if (someEmployee instanceof FullTimeEmployee) {
                    model.addElement("FullTime Employee");
                    FullTimeEmployee someFTE = (FullTimeEmployee) someEmployee;
                    String[] FTE_Component = someFTE.getFTEComponent();
                    for (int i = 0; i < FTE_Component.length; i++) {
                        model.addElement(FTE_Component[i]);
                    }
                    Search_Display.setModel(model);
                } else if (someEmployee instanceof PartTimeEmployee) {
                    model.addElement("PartTime Employee");
                    PartTimeEmployee somePTE = (PartTimeEmployee) someEmployee;
                    String[] PTE_Component = somePTE.getPTEComponent();
                    for (int i = 0; i < PTE_Component.length; i++) {
                        model.addElement(PTE_Component[i]);
                    }
                    Search_Display.setModel(model);
                }

            }
        }
    }//GEN-LAST:event_SearchForEmployeeActionPerformed

    private void EditEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditEmployeeActionPerformed
        // TODO add your handling code here:
        boolean invalid = false;
        int searchInput = -1;
        try {
            searchInput = Integer.parseInt(Search_Input.getText());
            System.out.println(searchInput);
        } catch (NumberFormatException NumberFormatException) {
            invalid = true;
        }

        if (invalid == true) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
        } else {
            int targetIndex = theHT.searchTable(searchInput);
            System.out.println(targetIndex);

            if (targetIndex == -1) {
                Search_Display.setModel(new javax.swing.AbstractListModel() {
                    String[] strings = {"Employee Not Found"};

                    @Override
                    public int getSize() {
                        return strings.length;
                    }

                    @Override
                    public Object getElementAt(int i) {
                        return strings[i];
                    }
                });
            } else {
                int targetBucket = theHT.calcBucket(searchInput);
                System.out.println(targetBucket);
                EmployeeInfo someEmployee = theHT.buckets[targetBucket].get(targetIndex);

                if (someEmployee instanceof FullTimeEmployee) {
                    FullTimeEmployee someFTE = (FullTimeEmployee) someEmployee;
                    Edit_FTE(someFTE);
                    Search_Employee_Panel.setVisible(false);
                    Edit_FTE_Panel.setVisible(true);
                } else if (someEmployee instanceof PartTimeEmployee) {
                    PartTimeEmployee somePTE = (PartTimeEmployee) someEmployee;
                    Edit_PTE(somePTE);
                    Search_Employee_Panel.setVisible(false);
                    Edit_PTE_Panel.setVisible(true);

                }
            }
        }
    }//GEN-LAST:event_EditEmployeeActionPerformed

