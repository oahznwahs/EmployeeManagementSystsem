    private void DeleteEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteEmployeeActionPerformed
        // TODO add your handling code here:
        boolean invalid = false;
        int searchInput = -1;
        try {
            searchInput = Integer.parseInt(Search_Input.getText());
            System.out.println(searchInput);
        } catch (NumberFormatException NumberFormatException) {
            invalid = true;
        }

        if (invalid == true) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
        } else {
            int targetIndex = theHT.searchTable(searchInput);
            if (targetIndex == -1) {
                Search_Display.setModel(new javax.swing.AbstractListModel() {
                    String[] strings = {"Employee Not Found"};

                    @Override
                    public int getSize() {
                        return strings.length;
                    }

                    @Override
                    public Object getElementAt(int i) {
                        return strings[i];
                    }
                });
            } else {
                theHT.removeFromTable(searchInput);
                Search_Display.setModel(new javax.swing.AbstractListModel() {
                    String[] strings = {"Employee Removed"};

                    @Override
                    public int getSize() {
                        return strings.length;
                    }

                    @Override
                    public Object getElementAt(int i) {
                        return strings[i];
                    }
                });
            }
            
        }
    }//GEN-LAST:event_DeleteEmployeeActionPerformed

